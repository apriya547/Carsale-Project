    private void mtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mtableMouseClicked
        int ri=mtable.getSelectedRow();
        if(ri!=-1){
            
            int id=(Integer)mtable.getValueAt(ri,0);
            String manufacture=(String)mtable.getValueAt(ri, 1);
             String model=(String)mtable.getValueAt(ri, 2);
            String year=(String)mtable.getValueAt(ri, 3);
             String carname=(String)mtable.getValueAt(ri, 4);
            
            int price=(Integer)mtable.getValueAt(ri, 5);
            String cartype=(String)mtable.getValueAt(ri, 6);
            String kmtravelled=(String)mtable.getValueAt(ri, 7);
            String extrainformation=(String)mtable.getValueAt(ri, 8);
            cid.setText(id+"");
          
            m1.setText(manufacture);
            mod1.setText(model);
            
            y1.setYear(Integer.parseInt(year));
            c1.setText(carname);
            p1.setText(price+"");
            t1.setSelectedItem(cartype);
            km1.setText(kmtravelled);
            ex1.setText(extrainformation);
           
                
                }
            
        
    }//GEN-LAST:event_mtableMouseClicked

    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed
         try{
              int id=Integer.parseInt(cid.getText());
        String manufacture=m1.getText();
        String model=mod1.getText();
        
        int dt=y1.getYear();
        String carname=(String)c1.getText();
        int price=(Integer.parseInt(p1.getText()));
        String cartype=(String)t1.getSelectedItem();
        String kmtravellled=km1.getText();
        String extrainformation=ex1.getText();
        
    
            
           
            my.dbConnect.updateClient.setString(1, manufacture);
            my.dbConnect.updateClient.setString(2, model);
            my.dbConnect.updateClient.setInt(3, dt);
            my.dbConnect.updateClient.setString(4, carname);
            my.dbConnect.updateClient.setInt(5, price);
            my.dbConnect.updateClient.setString(6, cartype);
            my.dbConnect.updateClient.setString(7, kmtravellled);
            my.dbConnect.updateClient.setString(8, extrainformation);
            my.dbConnect.updateClient.setInt(9, id);
            my.dbConnect.updateClient.executeUpdate();
            tKeyReleased(null);
            JOptionPane.showMessageDialog(null, "Client Updated Successfully !");
       
      
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    

    }//GEN-LAST:event_b1ActionPerformed

