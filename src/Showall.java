    private void tKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tKeyReleased
         try{
           javax.swing.table.DefaultTableModel dtm=( javax.swing.table.DefaultTableModel )mtable
                   .getModel();
           int rc=dtm.getRowCount();
           while(rc--!=0){
               dtm.removeRow(0);
               
           }
           String s=t.getText();
           my.dbConnect.getClient.setString(1,"%"+s+"%");
           ResultSet rs=my.dbConnect.getClient.executeQuery();
           while(rs.next()){
               java.util.Vector r=new java.util.Vector();
                r.add(rs.getInt("cid"));
               r.add(rs.getString("manufacture"));
                r.add(rs.getString("model"));
               r.add(rs.getString("year"));
               r.add(rs.getString("carname"));
               
               r.add(rs.getInt("price"));
               r.add(rs.getString("cartype"));
               r.add(rs.getString("kmtravelled"));
               r.add(rs.getString("extrainformation"));
               
               dtm.addRow(r);
           }
           
           
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null,ex);
           
       }
                            
               
        
    }//GEN-LAST:event_tKeyReleased

